    /** Delegate. */
    private final DefaultFontLetter delegate;

    /**
     * Construct this object.
     * 
     * @param name Name.
     * @param value Value.
     */
    public CerthFontLetter(final String name, final String value)
    {
        delegate = new DefaultFontLetter(ordinalCount, name, value);

        if (VALUES == null)
        {
            VALUES = new FontLetterList<CerthFontLetter>();
        }

        VALUES.add(this);
        ordinalCount++;
    }

    @Override
    public boolean equals(final Object object)
    {
        boolean answer = false;

        if (object == this)
        {
            answer = true;
        }
        else if (object == null)
        {
            answer = false;
        }
        else if (getClass() != object.getClass())
        {
            answer = false;
        }
        else
        {
            final CerthFontLetter another = (CerthFontLetter)object;

            answer = delegate.equals(another.delegate);
        }

        return answer;
    }

