    /** Ordinal count. */
    private static int ordinalCount = 0;

    /** Values. */
    private static RuneList<CerthRune> VALUES;

    /**
     * @param number Cirth number.
     * 
     * @return certh.
     */
    public final static CerthRune valueOfNumber(final int number)
    {
        CerthRune answer = null;

        final RuneList<CerthRune> values = values();

        if ((0 < number) && (number <= values.size()))
        {
            answer = values.get(number - 1);
        }

        return answer;
    }

    /**
     * @return a copy of the list of rune values.
     */
    public static final RuneList<CerthRune> values()
    {
        return new RuneList<CerthRune>(VALUES);
    }

    /** Delegate. */
    private final DefaultRune delegate;

    /**
     * Construct this object.
     * 
     * @param name Name.
     */
    private CerthRune(final String name)
    {
        final String displayName = name.toLowerCase();
        delegate = new DefaultRune(ordinalCount, name, displayName, name);

        if (VALUES == null)
        {
            VALUES = new RuneList<CerthRune>();
        }

        VALUES.add(this);
        ordinalCount++;
    }

    @Override
    public String displayName()
    {
        return delegate.displayName();
    }

    @Override
    public boolean equals(final Object object)
    {
        boolean answer = false;

        if (object == this)
        {
            answer = true;
        }
        else if (object == null)
        {
            answer = false;
        }
        else if (getClass() != object.getClass())
        {
            answer = false;
        }
        else
        {
            final CerthRune another = (CerthRune)object;

            answer = delegate.equals(another.delegate);
        }

        return answer;
    }

